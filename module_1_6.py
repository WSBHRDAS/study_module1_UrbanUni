my_string = input ("Введите строку с пробелами: ")

print("Количество символов введенной строки: ", len(my_string))

print("Введенная строка в верхнем регистре: ", my_string.upper())

print("Введенная строка в нижнем регистре: ", my_string.lower())

print("Введенная строка с удаленными пробелами: ,", my_string.replace(' ',''))

print("Первый символ строки: ", my_string[0])

print ("Последний символ строки: ", my_string[-1])

